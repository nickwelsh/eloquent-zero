it('generates many-to-many relationships from belongs-to-many relations', function () {
    $outputPath = sys_get_temp_dir().'/eloquent-zero-many-to-many-test.ts';

    File::delete($outputPath);

    Schema::create('members', function (Blueprint $table): void {
        $table->string('id')->primary();
    });

    Schema::create('groups', function (Blueprint $table): void {
        $table->string('id')->primary();
    });

    Schema::create('group_member', function (Blueprint $table): void {
        $table->string('member_id');
        $table->string('group_id');
        $table->primary(['member_id', 'group_id']);
        $table->foreign('member_id')->references('id')->on('members');
        $table->foreign('group_id')->references('id')->on('groups');
    });

    config()->set('eloquent-zero.output_path', $outputPath);

    $this->artisan('generate:zero-schema', [
        '--model' => [Member::class, Group::class],
    ])->assertSuccessful();

    $contents = File::get($outputPath);

    expect($contents)
        ->toContain("const groupMember = table('groupMember')")
        ->toContain('groups: many(')
        ->toContain('destSchema: groupMember')
        ->toContain("destField: ['memberId']")
        ->toContain('destSchema: group')
        ->toContain("destField: ['id']")
        ->toContain('members: many(');
});

it('forces required foreign keys back into zero columns allowlists', function () {
    $outputPath = sys_get_temp_dir().'/eloquent-zero-zero-columns-test.ts';

    File::delete($outputPath);
    Schema::dropIfExists('scoped_comments');
    Schema::dropIfExists('blog_posts');

    Schema::create('blog_posts', function (Blueprint $table): void {
        $table->string('id')->primary();
        $table->string('title');
    });

    Schema::create('scoped_comments', function (Blueprint $table): void {
        $table->string('id')->primary();
        $table->string('blog_post_id');
        $table->string('title');
        $table->string('secret_text');
        $table->foreign('blog_post_id')->references('id')->on('blog_posts');
    });

    config()->set('eloquent-zero.output_path', $outputPath);

    $this->artisan('generate:zero-schema', [
        '--model' => [BlogPost::class, ScopedComment::class],
    ])
        ->expectsOutputToContain('Forcing column [scoped_comments.blog_post_id] into schema because it is required.')
        ->assertSuccessful();

    expect(File::get($outputPath))
        ->toContain("blogPostId: string().from('blog_post_id')")
        ->not->toContain('secretText');
});

