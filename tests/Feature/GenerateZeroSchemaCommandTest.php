it('generates a zero schema file for an explicit model', function () {
    $outputPath = sys_get_temp_dir().'/eloquent-zero-schema-test.ts';

    File::delete($outputPath);

    config()->set('eloquent-zero.output_path', $outputPath);

    $this->artisan('generate:zero-schema', [
        '--model' => [BlogPost::class],
    ])->assertSuccessful();

    expect(File::exists($outputPath))->toBeTrue();

    $contents = File::get($outputPath);

    expect($contents)
        ->toContain('// @generated by eloquent-zero')
        ->toContain("const blogPost = table('blogPosts')")
        ->toContain("  .from('blog_posts')")
        ->toContain('id: string()')
        ->toContain('title: string()')
        ->toContain("  .primaryKey('id')")
        ->toContain('export const schema = createSchema({')
        ->toContain('export type Schema = Row<typeof schema>;')
        ->toContain("export type BlogPost = Schema['blogPosts'];")
        ->toContain('export const zql = createBuilder(schema)');
});

