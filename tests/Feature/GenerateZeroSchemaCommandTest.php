it('throws early when there are pending migrations', function () {
    $migrationPath = sys_get_temp_dir().'/eloquent-zero-pending-migrations';

    File::ensureDirectoryExists($migrationPath);
    File::put(
        $migrationPath.'/2099_01_01_000000_create_pending_table.php',
        <<<'PHP'
        <?php

        use Illuminate\Database\Migrations\Migration;
        use Illuminate\Database\Schema\Blueprint;
        use Illuminate\Support\Facades\Schema;

        return new class extends Migration
        {
            public function up(): void
            {
                Schema::create('pending_table', function (Blueprint $table): void {
                    $table->id();
                });
            }
        };
        PHP
    );

    app('migrator')->path($migrationPath);

    expect(fn () => Artisan::call('generate:zero-schema', [
        '--model' => [BlogPost::class],
        '--path' => sys_get_temp_dir().'/eloquent-zero-pending-migration-test.ts',
    ]))->toThrow(RuntimeException::class, 'Database schema may be out of date. Run `php artisan migrate` before generating a Zero schema.');
});
